let _lang = 'en';

export function setLanguage(lang) {
  if (STRINGS[lang]) _lang = lang;
  try { localStorage.setItem('dp_lang', lang); } catch (_) {}
}

export function getLanguage() { return _lang; }

export function detectLanguage(hint) {
  try {
    const saved = localStorage.getItem('dp_lang');
    if (saved && STRINGS[saved]) { _lang = saved; return; }
  } catch (_) {}
  const sys = hint || navigator.language?.slice(0, 2) || 'en';
  _lang = STRINGS[sys] ? sys : 'en';
}

export function t(key) {
  return (STRINGS[_lang] ?? STRINGS.en)[key] ?? STRINGS.en[key] ?? key;
}
