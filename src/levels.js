/**
 * Level definitions for Diamond Painting game.
 *
 * Each level:
 *   id       - unique number
 *   name     - display name
 *   width    - grid columns
 *   height   - grid rows
 *   palette  - array of { id, color, label }  (id 0 = background / empty)
 *   cells    - flat Uint8Array of length width*height; value = palette id (0 = skip)
 */

// Helper: build a flat cells array from a 2-D array of rows
function rows(grid) {
  return new Uint8Array(grid.flat());
}

